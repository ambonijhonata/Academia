    private void carregarDadosEdicao() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        txtNomeAluno.setText(aluno.getAluno());
        txtDataNascimento.setText(sdf.format(aluno.getDataNascimento()));

        switch (aluno.getSexo()) {
            case 'M':
                jRadioButtonMasculino.setSelected(true);
                break;
            case 'F':
                jRadioButtonFeminino.setSelected(true);
                break;
        }
        txtCelular.setText(aluno.getCelular());
        txtTelefone.setText(aluno.getTelefone());
        txtEmail.setText(aluno.getEmail());
        txtEndereco.setText(aluno.getEndereco());
        txtNumero.setText(aluno.getNumero());
        txtBairro.setText(aluno.getBairro());
        String cidade = aluno.getCidade().getCidade();
        jComboBoxCidade.setSelectedItem(aluno.getCidade().getCidade());
        txtCEP.setText(aluno.getCep());
        txtComplemento.setText(aluno.getComplemento());
        txtObservacao.setText(aluno.getObservacao());
        isEdicao = true;
    }

