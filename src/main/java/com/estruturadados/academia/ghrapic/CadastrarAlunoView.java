    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed

        // TODO add your handling code here:            
        if (txtNomeAluno.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor informe o nome.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtNomeAluno.requestFocus();
            return;
        } else if (txtDataNascimento.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Por favor informe a data de nascimento.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtDataNascimento.requestFocus();
            return;
        } else if (txtCelular.getText().equals("(  )     -    ")) {
            JOptionPane.showMessageDialog(null, "Por favor informe o celular.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtCelular.requestFocus();
            return;
        } else if (txtTelefone.getText().equals("(  )    -    ")) {
            JOptionPane.showMessageDialog(null, "Por favor informe o telefone.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtTelefone.requestFocus();
            return;
        } else if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor informe o email.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtEmail.requestFocus();
            return;
        } else if (txtEndereco.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor informe o endereço.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtEndereco.requestFocus();
            return;
        } else if (txtNumero.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor informe o número.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtNumero.requestFocus();
            return;
        } else if (txtBairro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor informe o bairro.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtBairro.requestFocus();
            return;
        } else if (txtCEP.getText().equals("     -   ")) {
            JOptionPane.showMessageDialog(null, "Por favor informe o CEP.", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtCEP.requestFocus();
            return;
        }
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            aluno.setAluno(txtNomeAluno.getText());
            aluno.setDataNascimento(new java.sql.Date(sdf.parse(txtDataNascimento.getText()).getTime()));

            if (jRadioButtonMasculino.isSelected()) {
                aluno.setSexo('M');
            } else {
                aluno.setSexo('F');
            }

            aluno.setCelular(txtCelular.getText());
            aluno.setTelefone(txtTelefone.getText());
            aluno.setEmail(txtEmail.getText());
            aluno.setEndereco(txtEndereco.getText());
            aluno.setNumero(txtNumero.getText());
            aluno.setBairro(txtBairro.getText());            

            for (Cidade c : listaCidades) {
                if (c.getCidade().equals(jComboBoxCidade.getSelectedItem().toString())) {
                    aluno.setCidade(c);
                    break;
                }
            }

            aluno.setCep(txtCEP.getText());
            aluno.setComplemento(txtComplemento.getText());
            aluno.setObservacao(txtObservacao.getText());

            if (!isEdicao) {
                if (controller.cadastrarAluno(aluno)) {
                    JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso.", "Atenção", JOptionPane.INFORMATION_MESSAGE);                    
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao cadastrar aluno.", "Atenção", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                if(controller.editarAluno(aluno)){
                    JOptionPane.showMessageDialog(null, "Aluno atualizado com sucesso.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Erro ao atualizar aluno.", "Atenção", JOptionPane.ERROR_MESSAGE);
                }
            }
            this.dispose();

        } catch (ParseException ex) {
        }
    }//GEN-LAST:event_btnGravarActionPerformed

